/* CICLO FOR: CICLO CONTROLADO */

for ( let i = 1; i < 6; i++){
    console.log(i);
}

/* CICLO WHILE */

let control =0;

while (control < 5) {
    console.log("while: " + control);
    control++;
}


/* CICLO DO WHILE */

let numero = 0;
do{
    console.log("Do...While: " + numero);
    numero++;
}while(numero < 5 );