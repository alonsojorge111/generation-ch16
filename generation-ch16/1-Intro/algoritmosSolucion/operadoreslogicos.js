//Operadores Logicos
/*
And (&&) "Hacer las dos cosas" (Y) true y true = true // true y false = false
Or (||) "Puedes hacer una cosa, una cosa u otra" (O) true y true = true // true y false = true
Not (!) "No" "Cambia el booleano"
*/
console.log(true&&true);
console.log(false);
console.log(true&&false);
console.log(false&&false);
console.log(true||true);
console.log(true||false);
console.log(false||true);
console.log(false||false);
console.log(!(false||true));
console.log(!(false&&true));